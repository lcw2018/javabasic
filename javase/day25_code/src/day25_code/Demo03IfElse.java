public class Demo03IfElse {


    public static void main(String[] args) {


        int num = 6666;

        if (num % 2 == 0) { //如果除以2，余数为0，则为偶数

            System.out.println("偶数");


        } else {


            System.out.println("基数");


        }


    }


}
